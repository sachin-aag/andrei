import { generateText, Output } from "ai";
import type { LanguageModel } from "ai";
import { z } from "zod";
import type { DedupedReasonSample } from "@/lib/eval/bulk-eval-aggregates";
import { truncateOneLine } from "@/lib/eval/bulk-eval-aggregates";
import { CRITERIA_EVAL_GOOGLE_MODEL_ID, CRITERIA_EVAL_SEED } from "@/lib/ai/evaluate";

const classificationSchema = z.object({
  assignments: z.array(
    z.object({
      reasonId: z.number().int(),
      /** Zero or more labels from the supplied bucket list. */
      buckets: z.array(z.string().min(1).max(120)),
    })
  ),
});

export type ReasoningPatternRow = {
  patternLabel: string;
  occurrences: number;
  topCriterionKeys: string[];
  exampleReasoning: string;
};

/** LLM settings for reasoning-bucket classification in bulk HTML reports. */
export const REASONING_BUCKET_LAYER_LLM = {
  modelId: CRITERIA_EVAL_GOOGLE_MODEL_ID as string,
  provider:
    "@ai-sdk/google · Vercel AI SDK generateText (`ai` package) + structured output (`Output.object`)",
  temperature: 0,
  maxOutputTokens: 8192 as const,
  schemaDescription:
    "assignments[].reasonId + buckets[] (multi-label; labels restricted to allowed lists)",
  passes: 2,
  passLabels: "specific issue buckets, then generic follow-up themes",
} as const;

export type LayeredReasoningPatterns = {
  specificRows: ReasoningPatternRow[];
  genericRows: ReasoningPatternRow[];
  usedSpecificFallback: boolean;
  usedGenericFallback: boolean;
};

/**
 * Concrete labels observed in the current sample report family.
 * The LLM may assign zero or many labels from this list to each reasoning.
 */
const SPECIFIC_BUCKETS = [
  "missing employee id",
  "missing date/time",
  "missing SOP section",
  "missing location",
  "missing initial scope",
  "missing equipment/system id",
  "missing attachment reference",
  "missing regulatory notification rationale",
  "missing analysis conclusion",
  "incomplete 5-Why chain",
  "missing root cause levels",
  "incomplete impact assessment",
  "missing responsible person",
  "missing due date",
  "missing tracking number",
  "missing expected outcome",
  "missing effectiveness verification",
  "weak corrective action",
  "missing preventive action",
  "missing preventive action linkage",
  "missing final comments",
  "missing lot disposition rationale",
  "incomplete final conclusion",
  "vague or unsupported rationale",
  "irrelevant action",
  "truncated sentence",
] as const;

/**
 * Broader layer for management follow-up themes. This is intentionally more generic
 * than the specific bucket list and is generated by a separate LLM call.
 */
const GENERIC_BUCKETS = [
  "missing data",
  "incomplete or truncated content",
  "incoherent reasoning",
  "unsupported conclusion",
  "traceability gap",
  "weak root cause analysis",
  "weak CAPA definition",
  "weak impact assessment",
  "readability issue",
  "irrelevant or mismatched action",
  "regulatory rationale gap",
] as const;

/** Multi-label classify reasonings into concrete and generic bucket layers. */
export async function classifyDedupedReasoningsWithLLM(params: {
  model: LanguageModel;
  deduped: DedupedReasonSample[];
}): Promise<LayeredReasoningPatterns> {
  const { deduped, model } = params;
  if (deduped.length === 0) {
    return {
      specificRows: [],
      genericRows: [],
      usedSpecificFallback: false,
      usedGenericFallback: false,
    };
  }

  const specific = await classifyWithBuckets({
    model,
    deduped,
    buckets: [...SPECIFIC_BUCKETS],
    layerName: "specific issue bucket",
    instructions:
      "Use concrete short labels such as missing employee id, missing date/time, missing due date, or incomplete 5-Why chain. " +
      "Do not force classification. Assign no buckets when none of the supplied labels apply. A reasoning can belong to multiple buckets.",
  });
  const generic = await classifyWithBuckets({
    model,
    deduped,
    buckets: [...GENERIC_BUCKETS],
    layerName: "generic follow-up theme",
    instructions:
      "Use broad management themes such as missing data, incoherent reasoning, weak CAPA definition, or traceability gap. " +
      "Do not force classification. Assign no buckets when none of the supplied labels apply. A reasoning can belong to multiple buckets.",
  });

  return {
    specificRows: specific.rows,
    genericRows: generic.rows,
    usedSpecificFallback: specific.usedFallback,
    usedGenericFallback: generic.usedFallback,
  };
}

async function classifyWithBuckets(params: {
  model: LanguageModel;
  deduped: DedupedReasonSample[];
  buckets: string[];
  layerName: string;
  instructions: string;
}): Promise<{ rows: ReasoningPatternRow[]; usedFallback: boolean }> {
  const { buckets, deduped, instructions, layerName, model } = params;
  const allowed = new Set(buckets);
  const lines = deduped.map((d) => {
    const critTop = entriesSortedDesc(d.criterionWeights)[0]?.[0] ?? "";
    const short = truncateOneLine(d.representativeText, 260);
    return `id=${d.id}\toccurrences=${d.weight}\tprimaryCriterion=${critTop}\t"${short}"`;
  });

  try {
    const result = await generateText({
      model,
      temperature: REASONING_BUCKET_LAYER_LLM.temperature,
      maxOutputTokens: REASONING_BUCKET_LAYER_LLM.maxOutputTokens,
      providerOptions: {
        google: { seed: CRITERIA_EVAL_SEED },
      },
      output: Output.object({ schema: classificationSchema }),
      system:
        `You classify pharmaceutical QA deviation review reasonings into ${layerName}s. ` +
        "Use ONLY the supplied bucket labels. Do not create new labels. " +
        "Each reason can have zero, one, or many buckets. Not every reason needs to be classified. " +
        instructions,
      prompt:
        `Allowed buckets:\n${buckets.map((b) => `- ${b}`).join("\n")}\n\n` +
        `Classify these reason buckets. Return assignments only for ids you classify; omit ids with no matching bucket.\n\n` +
        `${lines.join("\n")}`,
    });

    const parsed =
      result.experimental_output ??
      (() => {
        try {
          return classificationSchema.parse(JSON.parse(result.text));
        } catch {
          return null;
        }
      })();

    if (!parsed) {
      return { rows: fallbackClassify(deduped, buckets), usedFallback: true };
    }

    const byId = new Map(deduped.map((d) => [d.id, d]));
    const bucketMap = new Map<
      string,
      {
        occurrences: number;
        criterionWeights: Record<string, number>;
        exampleReasoning: string;
      }
    >();

    for (const assignment of parsed.assignments) {
      const item = byId.get(assignment.reasonId);
      if (!item) continue;
      const uniqueBuckets = [...new Set(assignment.buckets)].filter((b) =>
        allowed.has(b)
      );
      for (const bucket of uniqueBuckets) {
        addToBucket(bucketMap, bucket, item);
      }
    }

    const rows = rowsFromBucketMap(bucketMap);
    return { rows, usedFallback: rows.length === 0 };
  } catch {
    return { rows: fallbackClassify(deduped, buckets), usedFallback: true };
  }
}

function addToBucket(
  bucketMap: Map<
    string,
    {
      occurrences: number;
      criterionWeights: Record<string, number>;
      exampleReasoning: string;
    }
  >,
  bucket: string,
  item: DedupedReasonSample
) {
  const existing =
    bucketMap.get(bucket) ??
    ({
      occurrences: 0,
      criterionWeights: {},
      exampleReasoning: "",
    } satisfies {
      occurrences: number;
      criterionWeights: Record<string, number>;
      exampleReasoning: string;
    });

  existing.occurrences += item.weight;
  for (const [k, v] of Object.entries(item.criterionWeights)) {
    existing.criterionWeights[k] = (existing.criterionWeights[k] ?? 0) + v;
  }
  const t = item.representativeText.trim();
  if (!existing.exampleReasoning || (t && t.length < existing.exampleReasoning.length)) {
    existing.exampleReasoning = t;
  }
  bucketMap.set(bucket, existing);
}

function rowsFromBucketMap(
  bucketMap: Map<
    string,
    {
      occurrences: number;
      criterionWeights: Record<string, number>;
      exampleReasoning: string;
    }
  >
): ReasoningPatternRow[] {
  return [...bucketMap.entries()]
    .map(([patternLabel, v]) => ({
      patternLabel,
      occurrences: v.occurrences,
      topCriterionKeys: entriesSortedDesc(v.criterionWeights)
        .slice(0, 4)
        .map(([k]) => k),
      exampleReasoning: v.exampleReasoning,
    }))
    .sort((a, b) => b.occurrences - a.occurrences);
}

function fallbackClassify(
  deduped: DedupedReasonSample[],
  buckets: string[]
): ReasoningPatternRow[] {
  const bucketMap = new Map<
    string,
    {
      occurrences: number;
      criterionWeights: Record<string, number>;
      exampleReasoning: string;
    }
  >();

  for (const item of deduped) {
    const text = item.representativeText.toLowerCase();
    for (const bucket of buckets) {
      if (fallbackMatchesBucket(text, bucket)) {
        addToBucket(bucketMap, bucket, item);
      }
    }
  }

  return rowsFromBucketMap(bucketMap);
}

function fallbackMatchesBucket(text: string, bucket: string): boolean {
  const containsAny = (needles: string[]) => needles.some((n) => text.includes(n));
  switch (bucket) {
    case "missing employee id":
      return containsAny(["employee id", "emp. id", "emp id"]);
    case "missing date/time":
      return containsAny(["date", "time", "hh:mm"]);
    case "missing SOP section":
      return containsAny(["sop", "section number", "section no"]);
    case "missing location":
      return containsAny(["location", "room", "area code"]);
    case "missing responsible person":
      return containsAny(["responsible person"]);
    case "missing due date":
      return containsAny(["due date"]);
    case "missing tracking number":
      return containsAny(["unique", "tracking number", "work order"]);
    case "incomplete 5-Why chain":
      return containsAny(["5-why", "why chain"]);
    case "missing root cause levels":
      return containsAny(["level 2", "level 3", "root cause"]);
    case "incomplete impact assessment":
    case "weak impact assessment":
      return containsAny(["impact assessment", "impact"]);
    case "truncated sentence":
    case "incomplete or truncated content":
      return containsAny(["incomplete", "truncated", "cut off", "abruptly"]);
    case "missing data":
      return containsAny(["missing", "not provided", "not specified", "absent"]);
    case "weak CAPA definition":
    case "weak corrective action":
      return containsAny(["corrective action", "capa", "action"]);
    case "readability issue":
      return containsAny(["readability", "logical flow", "grammatical"]);
    case "unsupported conclusion":
    case "vague or unsupported rationale":
      return containsAny(["rationale", "unsupported", "vague", "conclusion"]);
    case "traceability gap":
      return containsAny(["traceability", "identifier", "attachment", "sop"]);
    case "regulatory rationale gap":
      return containsAny(["regulatory notification", "regulatory"]);
    case "irrelevant action":
    case "irrelevant or mismatched action":
      return containsAny(["irrelevant", "unrelated", "mismatched"]);
    case "incoherent reasoning":
      return containsAny(["does not logically", "inconsistent", "ambiguity"]);
    default:
      return bucket
        .split(/\s+/)
        .filter((word) => word.length > 4)
        .some((word) => text.includes(word));
  }
}

function entriesSortedDesc(o: Record<string, number>): [string, number][] {
  return Object.entries(o).sort(([, a], [, b]) => b - a);
}
